            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•
            Bitwise operators<br /><br />
            <h3>• Arithmetic operators:</h3>
            <br />
            Arithmetic operators are used with numeric values to perform common
            mathematical operations. Ex. + (Addition), - (Subtraction), *
            (Multiplication), / (Division), % (Modulus), ** (Exponentiation), //
            (floor division).<br /><br />
            <h3>• Assignment operators:</h3>
            <br />
            Assignment operators are used to assign values to variables. Ex. =,
            +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, <<=, >>=. <br /><br />
            <h3>• Comparison operators:</h3>
            <br />
            Comparison operators are used to compare two values. Ex. ==, !=, <,
            >, <=, >=.<br />
            <br />
            <h3>• Logical operators:</h3>
            <br />
            Logical operators are used to combine conditional statements. Ex.
            and, or, not. <br /><br />
            <h3>• Identity operators:</h3>
            <br />
            Identity operators are used to compare the objects, not if they are
            equal, but if they are actually the same object, with the same
            memory location. Ex, is, is not.<br /><br />
            <h3>• Membership operators:</h3>
            <br />
            Membership operators are used to test if a sequence is presented in
            an object. Ex, in, not in.<br /><br />
            <h3>• Bitwise operators:</h3>
            <br />
            Bitwise operators are used to compare (binary) numbers. Ex, &(AND),
            |(OR), ^(XOR), ~(NOT), <<(Zero Fill Lift shift), >>(Signed Right
            Shift).<br />


            <br />
            Python is a widely-used general-purpose, high-level programming
            language. It was initially designed by Guido van Rossum in 1991 and
            developed by Python Software Foundation. It was mainly developed for
            emphasis on code readability, and its syntax allows programmers to
            express concepts in fewer lines of code.<br /><br />
            In the late 1980s, history was about to be written. It was that time
            when working on Python started. Soon after that, Guido Van Rossum
            began doing its application-based work in December of 1989 at
            Centrum Wiskunde & Informatica (CWI) which is situated in the
            Netherlands. It was started firstly as a hobby project because he
            was looking for an interesting project to keep him occupied during
            Christmas. The programming language in which Python is said to have
            succeeded is ABC Programming Language, which had interfacing with
            the Amoeba Operating System and had the feature of exception
            handling. He had already helped to create ABC earlier in his career
            and he had seen some issues with ABC but liked most of the features.
            After that what he did was really very clever. He had taken the
            syntax of ABC, and some of its good features. It came with a lot of
            complaints too, so he fixed those issues completely and had created
            a good scripting language that had removed all the flaws. The
            inspiration for the name came from BBC’s TV Show – ‘Monty Python’s
            Flying Circus’, as he was a big fan of the TV show and also he
            wanted a short, unique and slightly mysterious name for his
            invention and hence he named it Python! He was the “Benevolent
            dictator for life” (BDFL) until he stepped down from the position as
            the leader on 12th July 2018. For quite some time he used to work
            for Google, but currently, he is working at Dropbox.<br />
            <br />
            The language was finally released in 1991. When it was released, it
            used a lot fewer codes to express the concepts, when we compare it
            with Java, C++ & C. Its design philosophy was quite good too. Its
            main objective is to provide code readability and advanced developer
            productivity. When it was released it had more than enough
            capability to provide classes with inheritance, several core data
            types exception handling and functions. <br />

            <h2>PYTHON CASTING</h2>
            <br />
            <h3>Description:</h3>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;There may be times
            when you want to specify a type on to a variable. This can be done
            with casting. Python is an object-orientated language, and as such
            it uses classes to define data types, including its primitive types.
            <br /><br />
            <h4>
              Casting in python is therefore done using constructor functions:
            </h4>
            <br />
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• int() - constructs
            an integer number from an integer literal, a float literal (by
            removing all decimals), or a string literal (providing the string
            represents a whole number). <br />
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• float() -
            constructs a float number from an integer literal, a float literal
            or a string literal (providing the string represents a float or an
            integer). <br />
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;• str() - constructs
            a string from a wide variety of data types, including
            strings,integer literals and float literals. <br />

